import { Router } from 'express';
import crypto from 'crypto';
import { spotifyService } from '../services/spotify';

export const spotifyRouter = Router();
const pkceStore = new Map<string, string>();

function generateCodeVerifier(): string {
  return crypto.randomBytes(32).toString('base64url');
}

function generateCodeChallenge(verifier: string): string {
  return crypto.createHash('sha256').update(verifier).digest('base64url');
}

spotifyRouter.get('/auth-url', (req, res) => {
  const state = crypto.randomBytes(16).toString('hex');
  const codeVerifier = generateCodeVerifier();
  const codeChallenge = generateCodeChallenge(codeVerifier);
  pkceStore.set(state, codeVerifier);
  res.json({
    authUrl: spotifyService.getAuthorizationUrl(state, codeChallenge),
    state,
  });
});

spotifyRouter.post('/exchange', async (req, res) => {
  const { code, state } = req.body;
  if (!code || !state) {
    return res.status(400).json({ error: 'Missing code or state' });
  }
  const codeVerifier = pkceStore.get(state);
  if (!codeVerifier) {
    return res.status(400).json({ error: 'Invalid or expired state' });
  }
  try {
    const tokens = await spotifyService.exchangeCodeForTokens(code, codeVerifier);
    pkceStore.delete(state);
    res.json(tokens);
  } catch (err: unknown) {
    const message = err instanceof Error ? err.message : 'Exchange failed';
    res.status(400).json({ error: message });
  }
});

spotifyRouter.post('/refresh', async (req, res) => {
  const { refresh_token } = req.body;
  if (!refresh_token) {
    return res.status(400).json({ error: 'Missing refresh_token' });
  }
  try {
    const tokens = await spotifyService.refreshAccessToken(refresh_token);
    res.json(tokens);
  } catch (err: unknown) {
    const message = err instanceof Error ? err.message : 'Refresh failed';
    res.status(400).json({ error: message });
  }
});

spotifyRouter.get('/me', async (req, res) => {
  const auth = req.headers.authorization;
  if (!auth?.startsWith('Bearer ')) {
    return res.status(401).json({ error: 'Missing or invalid Authorization header' });
  }
  const accessToken = auth.slice(7);
  try {
    const profile = await spotifyService.getCurrentUserProfile(accessToken);
    res.json(profile);
  } catch (err: unknown) {
    const message = err instanceof Error ? err.message : 'Failed to fetch profile';
    res.status(400).json({ error: message });
  }
});

